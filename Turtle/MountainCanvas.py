from turtle import *

setup(800, 600)
speed(0)
bgcolor("skyblue")

def SilentMove(x, y):
    penup()
    goto(x, y)
    pendown()

def drawTree(x, y):
    SilentMove(x, y)
    color("Brown")
    begin_fill()
    forward(5)
    left(90)
    forward(40)
    left(90)
    forward(10)
    left(90)
    forward(40)
    left(90)
    forward(10)
    end_fill()
    SilentMove(x, y+35)
    color("green4")
    begin_fill()
    circle(20)
    end_fill()

def drawTriangle(x, y, length, colorname):
    SilentMove(x, y)
    color(colorname)
    begin_fill()
    for i in range(3):
        forward(length)
        left(120)
    end_fill()

SilentMove(-400, 175)
